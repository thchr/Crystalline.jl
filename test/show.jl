# test print with nicely printed diff on failures (from https://github.com/invenia/PkgTemplates.jl/blob/master/test/runtests.jl)
using DeepDiffs: deepdiff
function print_diff(a, b)
    old = Base.have_color
    @eval Base have_color = true
    try
        println(deepdiff(a, b))
    finally
        @eval Base have_color = $old
    end
end

function test_show(expected::AbstractString, observed::AbstractString)
    if expected == observed
        @test true
    else
        print_diff(expected, observed)
        @test :expected == :observed
    end
end

function test_tp_show(v, expected::AbstractString)
    buffer = IOBuffer()
    # TODO: make sure the displaysize is large enough to hold the string,
    #       or include the ellipsis in the expected string
    io = IOContext(buffer, :displaysize => (50, 80))
    show(io, MIME"text/plain"(), v)
    observed = String(take!(buffer))
    test_show(observed, expected)
end


